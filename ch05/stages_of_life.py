# Exercise 5-6. Stages of Life

age = 23

if age < 2:
    article = "a"
    stage = "baby"
elif age < 4:
    article = "a"
    stage = "toddler"
elif age < 13:
    article = "a"
    stage = "kid"
elif age < 20:
    article = "a"
    stage = "teenager"
elif age < 65:
    article = "an"
    stage = "adult"
else:
    article = "an"
    stage = "elder"

print(f"The person is {article} {stage}.")
